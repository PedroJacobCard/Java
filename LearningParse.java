public class LearningParse {
    public static void main(String enter[]){
        int n1, n2, soma;
            n1 = Integer.parseInt(enter[0]);
            n2 = Integer.parseInt(enter[1]);
        soma = n1 + n2;
        System.out.println(n1 + " + " + n2 + " = " + soma);
        System.exit(0);
    }
}
